language = "Python Programming"

# len() method prints the length of the string
print(len(language))

# lower() method converts the string to lowercase
print(language.lower())

# upper() method converts the strin to uppercase
print(language.upper())

# title() method makes the First letter of each space seperated statement to uppercase
print(language.title())

# count() method counts the character repeated in the string, which is passed in the count method
# Note : the character argument should be passed compulsorily
print(language.count("P"))