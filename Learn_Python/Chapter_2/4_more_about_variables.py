# python allows multiple assignments in single line of statement
name, age = "Aditya", 20
print("Your name is : " + name)
print("Your age is : " + str(age))

# python also allows multiple input in single line of statement
name, age = input("Enter your name : "), input("Enter your age : ")
print("Your name is : " + name)
print("Your name is : " + age)

# all programming languages allows assignment of multiple variables to single value
x = y = z = 1
print(x, y, z)